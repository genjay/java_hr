
Select @In_STR REGEXP '^([0-9][0-9][0-9][0-9])$'; # 年份正則 0000-9999
Select @In_STR REGEXP '^[0-9]{4}$';  # 年份正則 0000-9999


Select @In_STR REGEXP '^([0-9][0-9][0-9][0-9])/?-?.?(0[1-9]|1[0-2])$'; # 年月 201401、2014-01、2014/01 皆可
Select @In_STR REGEXP '^([0-9]{4})/?-?.?(0[1-9]|1[0-2])$'; # 年月 201401、2014-01、2014/01 皆可


Select @In_STR REGEXP '^([0-9]{4})/?-?.?(0[1-9]|1[0-2])(/?-?.?)(0[1-9]|1[0-9]|2[0-9]|3[0-1])$'; # 年月日
 # 20140101、2014/01/01、2014-01-01 皆可
 # 但是無法判斷 2014/02/29 一樣會過


Select @In_STR REGEXP '^(([0-1][0-9])|([1-2][0-3])):?([0-5][0-9])$'; # 時間 0000~ 2359 需足四碼


Select @In_STR REGEXP '^[A-Za-z][1-2][0-9]{8}$'; # 台灣身份証簡易驗証